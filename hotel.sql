CREATE TABLE hotel_chain(
	chain_name VARCHAR(50) PRIMARY KEY,
	website VARCHAR(50) NOT NULL,
    num_hotels int NOT NULL,
    email VARCHAR(50) NOT NULL,
    phone VARCHAR(50) NOT NULL
	
);

CREATE TABLE central_office(
	office_id INT NOT NULL,
	chain_name VARCHAR(50) NOT NULL,
	street VARCHAR(50) NOT NULL,
    city VARCHAR(50) NOT NULL,
    country VARCHAR(50) NOT NULL, 
    phone VARCHAR(50) NOT NULL,
    PRIMARY KEY(office_id,chain_name),
    FOREIGN KEY(chain_name) REFERENCES hotel_chain(chain_name)
);

CREATE TABLE hotel(
	hotel_id INT NOT NULL,
    chain_name VARCHAR(50) NOT NULL,
    star_category INT NOT NULL 
		CHECK(star_category <= 5 and star_category >= 1),
	website VARCHAR(50) NOT NULL,
	street VARCHAR(50) NOT NULL,
    city VARCHAR(50) NOT NULL,
    country VARCHAR(50) NOT NULL, 
    email VARCHAR(50) NOT NULL,
    phone VARCHAR(50) NOT NULL,
    PRIMARY KEY(hotel_id, chain_name),
    FOREIGN KEY(chain_name) REFERENCES hotel_chain(chain_name)
);

CREATE TABLE manager(
	manager_id INT PRIMARY KEY,
    name VARCHAR(50) NOT NULL
);

CREATE TABLE hotel_manager(
	manager_id INT NOT NULL,
    hotel_id INT NOT NULL,
    PRIMARY KEY(manager_id, hotel_id),
    FOREIGN KEY(manager_id) REFERENCES manager(manager_id),
    FOREIGN KEY(hotel_id) REFERENCES hotel(hotel_id)
);

CREATE TABLE employee(
	employee_id INT PRIMARY KEY,
    ssn INT NOT NULL,
    f_name VARCHAR(50) NOT NULL,
    l_name VARCHAR(50) NOT NULL,
    street VARCHAR(50) NOT NULL,
    city VARCHAR(50) NOT NULL,
    country VARCHAR(50) NOT NULL, 
    date_of_reg DATE NOT NULL
);

CREATE TABLE hotel_employee(
	employee_id INT NOT NULL,
    hotel_id INT NOT NULL,
	PRIMARY KEY(employee_id, hotel_id),
    FOREIGN KEY(employee_id) REFERENCES employee(employee_id),
    FOREIGN KEY(hotel_id) REFERENCES hotel(hotel_id)
);

CREATE TABLE pos(
	position_id INT PRIMARY KEY,
    position_name VARCHAR(50) NOT NULL
);

CREATE TABLE works(
	position_id INT NOT NULL,
	employee_id INT NOT NULL,
    PRIMARY KEY(position_id, employee_id),
	FOREIGN KEY(position_id) REFERENCES pos(position_id),
    FOREIGN KEY(employee_id) REFERENCES employee(employee_id)
);

CREATE TABLE customer(
	customer_id INT PRIMARY KEY,
    ssn INT NOT NULL,
    f_name VARCHAR(50) NOT NULL,
    l_name VARCHAR(50) NOT NULL,
    street VARCHAR(50) NOT NULL,
    city VARCHAR(50) NOT NULL,
    country VARCHAR(50) NOT NULL, 
    date_of_reg DATE NOT NULL
);

CREATE TABLE hotel_customer(
	customer_id INT NOT NULL,
    hotel_id INT NOT NULL,
    PRIMARY KEY(customer_id, hotel_id),
    FOREIGN KEY(customer_id) REFERENCES customer(customer_id),
    FOREIGN KEY(hotel_id) REFERENCES hotel(hotel_id)
);

CREATE TABLE room(
	room_id INT PRIMARY KEY,
    price INT NOT NULL,
    capacity VARCHAR(10) NOT NULL
		CHECK(capacity IN ('single','double','queen','king')),
	view_type VARCHAR(20) NOT NULL
		CHECK(view_type in ('sea view','mountain view')),
	is_extendable VARCHAR(10) NOT NULL
		CHECK(is_extendable in ('true','false')),
    problems VARCHAR(20) NOT NULL
		CHECK(problems in ('true','false')),
    tv VARCHAR(20) NOT NULL
		CHECK(tv in ('true','false')),
    ac VARCHAR(20) NOT NULL
		CHECK(ac in ('true','false')),
    fridge VARCHAR(20) NOT NULL
		CHECK(fridge in ('true','false')),
    wifi VARCHAR(20) NOT NULL
		CHECK(wifi in ('true','false')),
    towels VARCHAR(20) NOT NULL
		CHECK(towels in ('true','false'))
);

CREATE TABLE hotel_room(
	hotel_id INT NOT NULL,
    room_id INT NOT NULL,
    PRIMARY KEY(hotel_id, room_id),
    FOREIGN KEY(hotel_id) REFERENCES hotel(hotel_id),
    FOREIGN KEY(room_id) REFERENCES room(room_id)
);

CREATE TABLE books(
	customer_id INT NOT NULL,
    room_id INT NOT NULL,
    PRIMARY KEY(customer_id, room_id),
    FOREIGN KEY(customer_id) REFERENCES customer(customer_id),
    FOREIGN KEY(room_id) REFERENCES room(room_id)
);

CREATE TABLE booking(
    booking_id INT NOT NULL,
	room_id INT NOT NULL,
    check_in_date DATE NOT NULL,
    check_out_date DATE NOT NULL,
    amount_paid INT NOT NULL,
    PRIMARY KEY(booking_id, room_id),
    FOREIGN KEY(room_id) REFERENCES room(room_id)
);

CREATE TABLE renting(
	renting_id INT NOT NULL,
    booking_id INT NOT NULL,
    check_in_date DATE NOT NULL,
    check_out_date DATE NOT NULL,
    PRIMARY KEY(renting_id, booking_id),
    FOREIGN KEY(booking_id) REFERENCES booking(booking_id)
);



